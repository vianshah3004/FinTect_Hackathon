async def generate_audio(text: str, voice: str, output_path: str):
    """Generate audio using edge-tts"""
    try:
        communicate = edge_tts.Communicate(text.strip(), voice)
        await communicate.save(output_path)
        print(f"  тЬУ Generated: {os.path.basename(output_path)}")
        return True
    except Exception as e:
        print(f"  тЬЧ Error: {e}")
        return False

async def main():
    print("=" * 60)
    print("ЁЯОЩя╕П Generating Dubbed Audio for Video Lessons")
    print("=" * 60)
    
    total_files = 0
    success_count = 0
    
    for category, transcripts in TRANSCRIPTS.items():
        print(f"\nЁЯУБ Category: {category.upper()}")
        category_dir = os.path.join(ASSETS_DIR, category)
        os.makedirs(category_dir, exist_ok=True)
        
        for lang_code, text in transcripts.items():
            voice = VOICES.get(lang_code)
            if not voice:
                continue
                
            output_file = os.path.join(category_dir, f"intro_{lang_code}.mp3")
            total_files += 1
            
            success = await generate_audio(text, voice, output_file)
            if success:
                success_count += 1
    
    print("\n" + "=" * 60)
    print(f"тЬЕ Generated {success_count}/{total_files} audio files")
    print("=" * 60)
    print("\nЁЯЪА Next steps:")
    print("1. Stop flutter app (press 'q')")
    print("2. Run 'flutter run' again to bundle new assets")
    print("3. Test video lessons with language selector!")

if __name__ == "__main__":
    asyncio.run(main())
